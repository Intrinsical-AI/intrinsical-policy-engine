SDIST_RELEASE_FILES = {
    "BOUNDARIES.md",
    "CHANGELOG.md",
    "LICENSE",
    "MANIFEST.in",
    "NOTICE",
    "PKG-INFO",
    "PROVENANCE.md",
    "README.md",
    "SECURITY.md",
    "pyproject.toml",
    # Generated by setuptools while assembling the source distribution.
    "setup.cfg",
}
SDIST_FORBIDDEN_ROOTS = {
    "demos",
    "frameworks",
    "private",
    "scripts",
    "tests",
    "workspace",
}
SDIST_FORBIDDEN_CANONICAL_COMPONENTS = {"private", "workspace"}
MARKDOWN_LINK = re.compile(r"\[[^]]+\]\(([^)]+)\)")


def _pyproject() -> dict:
    return tomllib.loads(PYPROJECT_PATH.read_text(encoding="utf-8"))


def _entrypoint() -> str:
    return str(_pyproject()["project"]["scripts"]["ipe"])


def _python_files(*roots: Path) -> list[Path]:
    return sorted(path for root in roots if root.exists() for path in root.rglob("*.py"))


def _absolute_imports_with_prefix(paths: list[Path], prefix: str) -> list[str]:
    found: list[str] = []
    for path in paths:
        tree = ast.parse(path.read_text(encoding="utf-8"), filename=str(path))
        for node in ast.walk(tree):
            if isinstance(node, ast.ImportFrom):
                module = node.module or ""
                if module == prefix or module.startswith(prefix + "."):
                    found.append(f"{path.relative_to(ROOT)}:{node.lineno}:{module}")
            elif isinstance(node, ast.Import):
                for alias in node.names:
                    if alias.name == prefix or alias.name.startswith(prefix + "."):
                        found.append(f"{path.relative_to(ROOT)}:{node.lineno}:{alias.name}")
    return found


def _require_2x_layout() -> None:
    if CANONICAL_PACKAGE_ROOT.exists():
        pytest.skip("2.x baseline no longer applies after the namespace move")


def test_2x_baseline_version_and_entrypoint_are_coherent() -> None:
    """Freeze the released baseline until the atomic namespace batch lands."""
    _require_2x_layout()
    config = _pyproject()
    project = config["project"]
    package_find = config["tool"]["setuptools"]["packages"]["find"]
    runtime_package = importlib.import_module("src")
    module_name, function_name = _entrypoint().split(":", maxsplit=1)
    main_module = importlib.import_module(module_name)

    assert project["name"] == "intrinsical-policy-engine"
    assert project["version"] == runtime_package.__version__
    assert _entrypoint() == "intrinsical_policy_engine.app.cli.main:main"
    assert package_find == {
        "where": ["."],
        "include": ["src*", "scripts*"],
        "namespaces": False,
    }
    assert callable(getattr(main_module, function_name))


def test_2x_baseline_module_cli_reports_distribution_version() -> None:
    _require_2x_layout()
    version = str(_pyproject()["project"]["version"])
    completed = subprocess.run(
        [sys.executable, "-m", "intrinsical_policy_engine.app.cli", "--version"],
        cwd=ROOT,
        text=True,
        capture_output=True,
        check=False,
    )

    assert completed.returncode == 0, completed.stderr
    assert completed.stdout.strip() == f"ipe {version}"


def test_2x_baseline_namespace_inventory_is_frozen() -> None:
    """Make a changed migration surface visible during the preparation phase."""
    _require_2x_layout()
    source_files = _python_files(SOURCE_ROOT)
    legacy_imports = _absolute_imports_with_prefix(source_files, "src")

    assert len(source_files) >= BASELINE_2X_MODULES
    assert len(list(SOURCE_ROOT.rglob("__init__.py"))) >= BASELINE_2X_PACKAGE_INITIALIZERS
    assert 0 < len(legacy_imports) <= BASELINE_2X_RUNTIME_LEGACY_IMPORTS


