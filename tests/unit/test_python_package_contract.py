@requires_wheel_3
def test_3x_wheel_contains_only_the_canonical_runtime_package() -> None:
    wheel = Path(str(TARGET_WHEEL)).expanduser().resolve()
    assert wheel.is_file(), f"IPE_3_0_WHEEL does not exist: {wheel}"

    with zipfile.ZipFile(wheel) as archive:
        names = set(archive.namelist())
        metadata_name = next(name for name in names if name.endswith(".dist-info/METADATA"))
        entry_points_name = next(
            name for name in names if name.endswith(".dist-info/entry_points.txt")
        )
        top_level_name = next(name for name in names if name.endswith(".dist-info/top_level.txt"))
        metadata = Parser().parsestr(archive.read(metadata_name).decode("utf-8"))
        entry_points = archive.read(entry_points_name).decode("utf-8")
        top_levels = {
            line.strip()
            for line in archive.read(top_level_name).decode("utf-8").splitlines()
            if line.strip()
        }

    assert metadata["Name"] == "intrinsical-policy-engine"
    assert metadata["Version"] == str(_pyproject()["project"]["version"])
    assert top_levels == {"intrinsical_policy_engine"}
    assert "ipe = intrinsical_policy_engine.app.cli.main:main" in entry_points
    assert "intrinsical_policy_engine/__init__.py" in names
    assert "intrinsical_policy_engine/__main__.py" in names
    assert "intrinsical_policy_engine/py.typed" in names
    assert not any(name.startswith(("src/", "scripts/", "tests/", "demos/")) for name in names)
    assert not any(".codex" in Path(name).parts for name in names)


@requires_sdist_3
def test_3x_sdist_contains_only_public_release_sources() -> None:
    sdist = Path(str(TARGET_SDIST)).expanduser().resolve()
    assert sdist.is_file(), f"IPE_3_0_SDIST does not exist: {sdist}"

    config = _pyproject()["project"]
    expected_version = str(config["version"])
    expected_root = f"intrinsical_policy_engine-{expected_version}"
    invalid_paths: list[str] = []

    with tarfile.open(sdist, mode="r:*") as archive:
        members = archive.getmembers()
        roots = {
            PurePosixPath(member.name).parts[0]
            for member in members
            if PurePosixPath(member.name).parts
        }
        assert roots == {expected_root}
        assert all(member.isfile() or member.isdir() for member in members)

        for member in members:
            archive_path = PurePosixPath(member.name)
            parts = archive_path.parts
            assert not archive_path.is_absolute()
            assert ".." not in parts
            relative_parts = parts[1:]
            if not relative_parts:
                continue

            first = relative_parts[0]
            if first.casefold() in SDIST_FORBIDDEN_ROOTS:
                invalid_paths.append(member.name)
                continue

            if len(relative_parts) == 1 and first in SDIST_RELEASE_FILES:
                continue
            if first == "docs":
                continue
            if first == "src":
                if len(relative_parts) == 1:
                    continue
                source_root = relative_parts[1]
                if source_root == "intrinsical_policy_engine.egg-info":
                    continue
                if source_root == "intrinsical_policy_engine" and not (
                    {part.casefold() for part in relative_parts[2:]}
                    & SDIST_FORBIDDEN_CANONICAL_COMPONENTS
                ):
                    continue
            invalid_paths.append(member.name)

        pkg_info_member = next(
            member
            for member in members
            if PurePosixPath(member.name) == PurePosixPath(expected_root, "PKG-INFO")
        )
        pkg_info_file = archive.extractfile(pkg_info_member)
        assert pkg_info_file is not None
        pkg_info = Parser().parsestr(pkg_info_file.read().decode("utf-8"))
        readme_file = archive.extractfile(f"{expected_root}/README.md")
        assert readme_file is not None
        readme = readme_file.read().decode("utf-8")

    archived_files = {
        PurePosixPath(*PurePosixPath(member.name).parts[1:]).as_posix()
        for member in members
        if member.isfile()
    }
    local_readme_links = {
        target.split("#", maxsplit=1)[0]
        for target in MARKDOWN_LINK.findall(readme)
        if target and not target.startswith(("#", "http://", "https://", "mailto:"))
    }

    assert invalid_paths == []
    assert pkg_info["Name"] == str(config["name"])
    assert pkg_info["Version"] == expected_version
    assert local_readme_links <= archived_files
